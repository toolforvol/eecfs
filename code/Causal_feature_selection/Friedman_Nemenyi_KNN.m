% ---------------------------
% Friedman 检验
% ---------------------------
[p, tbl, stats] = friedman(accuracies, 1, 'off');
fprintf('Friedman 检验 p 值: %.4f\n', p);

% ---------------------------
% 平均秩计算（0 视为最低）
% ---------------------------
ranks = zeros(size(accuracies));

for i = 1:size(accuracies, 1)
    row = accuracies(i, :);
    zero_mask = (row == 0);           % 找出零值位置
    valid_row = row(~zero_mask);      % 去掉零值
    k_valid = numel(valid_row);
    
    if k_valid > 0
        % 对非零部分排名（精度高→rank小）
        valid_ranks = tiedrank(-valid_row);
        
        % 将非零 rank 填回
        ranks(i, ~zero_mask) = valid_ranks;
    end
    
    % 将零值设为最后一名（比非零最大 rank 大 1）
    ranks(i, zero_mask) = k_valid + 1;
end

avg_ranks = mean(ranks);

fprintf('\n算法平均秩（rank 越小越好）:\n');
for i = 1:numel(algorithms)
    fprintf('%-10s: %.3f\n', algorithms{i}, avg_ranks(i));
end

% ---------------------------
% Nemenyi 事后检验
% ---------------------------
alpha = 0.05;
k = size(accuracies, 2);  % 算法数
N = size(accuracies, 1);  % 数据集数

if p < alpha
    fprintf('\n→ 拒绝原假设，算法之间存在显著差异。\n');
    fprintf('执行 Nemenyi 事后检验:\n');

    % 临界值 q_alpha 查 Nemenyi 表 (k=12, α=0.05)
    q_alpha = 3.314;
    CD = q_alpha * sqrt(k * (k + 1) / (6 * N));
    fprintf('\nNemenyi 临界差值 (CD): %.4f\n\n', CD);

    % 计算算法两两之间的平均秩差
    sig_pairs = {};
    for i = 1:k
        for j = i+1:k
            diff = abs(avg_ranks(i) - avg_ranks(j));
            if diff > CD
                sig_pairs{end+1,1} = sprintf('%s vs %s', algorithms{i}, algorithms{j});
                sig_pairs{end,2} = diff;
            end
        end
    end

    if isempty(sig_pairs)
        fprintf('→ 没有算法对之间的秩差超过临界值，差异不显著。\n');
    else
        fprintf('显著差异的算法对 (平均秩差 > CD):\n');
        for n = 1:size(sig_pairs,1)
            fprintf('  %-25s  平均秩差 = %.3f\n', sig_pairs{n,1}, sig_pairs{n,2});
        end
    end
else
    fprintf('\n→ 无法拒绝原假设，算法之间差异不显著。\n');
end

